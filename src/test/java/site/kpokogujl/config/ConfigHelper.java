package site.kpokogujl.config;

import org.aeonbits.owner.ConfigFactory;

public class ConfigHelper {

    public static String browserstackLogin() {

        return getConfig().browserstackLogin();
    }

    public static String browserstackPassword() {

        return getConfig().browserstackPassword();
    }

    public static String browsesrstackUrl() {

        return getConfig().browsesrstackUrl();
    }

    public static String appUrl() {

        return getConfig().appUrl();
    }

    private static TestsConfig getConfig() {

        return ConfigFactory.newInstance().create(TestsConfig.class);
    }
}
